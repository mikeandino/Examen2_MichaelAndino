    public void arbol() {
        DefaultTreeModel m = (DefaultTreeModel) jTree1.getModel();
        DefaultMutableTreeNode raiz = (DefaultMutableTreeNode) m.getRoot();
        raiz.removeAllChildren();
        for (Usuario user : aa.getU()) {
            DefaultMutableTreeNode nombret = new DefaultMutableTreeNode(user.getNombre());
            DefaultMutableTreeNode p = new DefaultMutableTreeNode("Nickname: " + user.getUsername());
            nombret.add(p);
            p = new DefaultMutableTreeNode("Edad: " + user.getEdad());
            nombret.add(p);
            p = new DefaultMutableTreeNode("Username: " + user.getUsername());
            nombret.add(p);
            p = new DefaultMutableTreeNode("Password: " + user.getPassword());
            nombret.add(p);
            raiz.add(nombret);
            m.reload();
        }
    }

    public void arbol2() {
        DefaultTreeModel m = (DefaultTreeModel) jTree2.getModel();
        DefaultMutableTreeNode raiz = (DefaultMutableTreeNode) m.getRoot();
        raiz.removeAllChildren();
        for (Album album : aa.getA()) {
            DefaultMutableTreeNode nombret = new DefaultMutableTreeNode(album.getNombre());
            DefaultMutableTreeNode p;
            for (Cancion cancion : album.getLista()) {
                p = new DefaultMutableTreeNode(cancion.getNombre());
                nombret.add(p);
            }
            raiz.add(nombret);
            m.reload();
        }
    }

    public void tabla() {
        DefaultTableModel m = new DefaultTableModel();
        m.addColumn("Cancion");
        m.addColumn("Artista");
        m.addColumn("Duracion");
        m.addColumn("Genero");
        Album temp = aa.getA().get(jComboBox3.getSelectedIndex());
        for (Cancion cancion : temp.getLista()) {
            Object[] fila = {cancion.getNombre(), cancion.getArtista(), cancion.getDuracion(), cancion.getGenero()};
            m.addRow(fila);
        }
        jTable1.setModel(m);
        jTable1.repaint();
    }

